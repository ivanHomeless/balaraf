     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(PageCreateRequest $request)
    {
        $item = false;
        if ($item) {
            return redirect()
                ->route('admin.cards.edit', $item->id)
                ->with(['success' => 'Успешно сохранено']);
